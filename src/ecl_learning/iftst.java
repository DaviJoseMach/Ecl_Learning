package ecl_learning;

// Declaração da classe chamada "iftst"
public class iftst {

    // Método principal "main"
    public static void main(String[] args) {
        // Declaração e inicialização da variável "nome" com o valor "Gomes"
        String nome = "Gabriel";

        // Estrutura condicional "if" para verificar se o valor de "nome" é igual a "Gabriel"
        if (nome.equals("Gabriel")) {
            // Se a condição for verdadeira, imprime "Acesso liberado"
            System.out.println("Acesso liberado");
        } else {
            // Se a condição for falsa, imprime "Tente novamente"
            System.out.println("Tente novamente");
        }
    }
}
